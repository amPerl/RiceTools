        public static int Adler32(byte[] data)
        {
            ulong num1 = 1UL;
            ulong num2 = 0UL;
            for (uint index = 0U; (long)index < (long)data.Length; ++index)
            {
                num1 = (num1 + (ulong)data[index]) % 65521UL;
                num2 = (num2 + num1) % 65521UL;
            }
            return (int)((long)num2 << 16 | (long)num1);
        }
